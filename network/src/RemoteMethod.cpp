#include "rd/network/RemoteMethod.h"

using namespace rd;
using namespace std;


RemoteMethod::RemoteMethod() { }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

RemoteMethod::RemoteMethod(string name, string sig, string help)
        : name(name) {
    this->_signature = sig;
    this->_help = help;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void RemoteMethod::setName(const string &name) {
    this->name = name;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const string &RemoteMethod::getName() const {
    return name;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

RemoteMethod::~RemoteMethod() { }
